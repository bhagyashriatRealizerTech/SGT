public class GoogleDriveUploadClass {

    public String gdfilename;
    public String filepath;
    public String foldername;
    public int gdID;
    public String gdtype;


    public String getGdfilename() {
        return gdfilename;
    }

    public void setGdfilename(String gdfilename) {
        this.gdfilename = gdfilename;
    }

    public String getFilepath() {
        return filepath;
    }

    public void setFilepath(String filepath) {
        this.filepath = filepath;
    }

    public String getFoldername() {
        return foldername;
    }

    public void setFoldername(String foldername) {
        this.foldername = foldername;
    }

    public int getGdID() {
        return gdID;
    }

    public void setGdID(int gdID) {
        this.gdID = gdID;
    }

    public String getGdtype() {
        return gdtype;
    }

    public void setGdtype(String gdtype) {
        this.gdtype = gdtype;
    }


}
